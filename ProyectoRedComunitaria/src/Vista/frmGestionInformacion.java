    public void listar() throws SQLException {
        tableModel.setRowCount(0); // Limpiar la tabla
        List<GestionInformacion> listado = gestionController.listar();
        listado.forEach((gi) -> {
            tableModel.addRow(new Object[]{gi.getId_gestion(), gi.getId_proyecto(), gi.getFechaGeneracion(), gi.getId_emprendedor()});
        });
    }
    
    public void listar_proyectos(List<Integer> listado_proyectos,JComboBox c) throws SQLException{
        DefaultComboBoxModel combo=new DefaultComboBoxModel();
        c.setModel(combo);
        for (int i = 0; i < listado_proyectos.size(); i++) {
            combo.addElement(listado_proyectos.get(i));
            
        }
    }
    
    public void listar_emprendedores(List<Integer> listado_emprendedores,JComboBox c) throws SQLException{
        DefaultComboBoxModel combo=new DefaultComboBoxModel();
        c.setModel(combo);
        for (int i = 0; i < listado_emprendedores.size(); i++) {
            combo.addElement(listado_emprendedores.get(i));
            
        }
    }

