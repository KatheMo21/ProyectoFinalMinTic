    private void eliminar(int id) throws SQLException {
        
        gestionController.eliminar(id);
        this.limpiarFormulario();
        listar();
        JOptionPane.showMessageDialog(this, "Registro eliminado exitosamente");
        inicializarBotonera();
    }

    private void actualizar() {
        int id = Integer.parseInt(txtId.getText());
        
        int id_proyecto = (int)(jComboIdProyecto.getSelectedItem());
        String fecha = txtFecha.getText();
        int id_emprendedor = (int)(jComboIdEmprendedor.getSelectedItem());
        
        
        
        GestionInformacion gestionInformacion = new GestionInformacion(id,id_proyecto,fecha,id_emprendedor);
        try {
            gestionController.actualizar(gestionInformacion);
            JOptionPane.showMessageDialog(this, "Registro Actualizado");
            listar();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void consultar() throws SQLException {
        int id = Integer.parseInt(txtBuscarID.getText());
        gestionInformacion = gestionController.consultar(id);
        if (gestionInformacion == null) {
            JOptionPane.showMessageDialog(this, "ID NO ENCONTRADO");
        } else {
            txtId.setText(String.valueOf(gestionInformacion.getId_gestion()));
            jComboIdProyecto.setSelectedItem(gestionInformacion.getId_proyecto());
            txtFecha.setText(gestionInformacion.getFechaGeneracion());
            jComboIdEmprendedor.setSelectedItem(gestionInformacion.getId_emprendedor());
            
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
            btnAgregar.setEnabled(false);
        }
    }

    private void limpiarFormulario() {
        txtId.setText("");
//        jComboIdPais.setText("");
//        jComboIdUsuario.setText("");
        txtFecha.setText("");
        txtBuscarID.setText("");
        inicializarBotonera();
    }

    private void inicializarBotonera() {
        btnAgregar.setEnabled(false);
        btnBuscar.setEnabled(false);
        
        if(this.tblTabla.getSelectedRow()>0 || this.tblTabla.getRowCount()>0){
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }else{
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }
    }

    private boolean validarVacios() {
        boolean validado = false;
        if ((!txtId.getText().trim().isEmpty())&& (!txtFecha.getText().trim().isEmpty())) {
            validado = true;
        }
        return validado;
    }
    
    private void agregar() {

        
        
        int id_proyecto = (int)(jComboIdProyecto.getSelectedItem());
        String fecha = txtFecha.getText();
        int id_emprendedor = (int)(jComboIdEmprendedor.getSelectedItem());
          
        
        GestionInformacion gestionInformacion = new GestionInformacion(0,id_proyecto,fecha,id_emprendedor);
        try {
            gestionController.agregar(gestionInformacion);
            JOptionPane.showMessageDialog(this, "Registro agregado");
            listar();
            limpiarFormulario();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

