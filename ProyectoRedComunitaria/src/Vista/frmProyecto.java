    private void eliminar(int id) throws SQLException {
        
        proyectoController.eliminar(id);
        this.limpiarFormulario();
        listar();
        JOptionPane.showMessageDialog(this, "Registro eliminado exitosamente");
        inicializarBotonera();
    }

    private void actualizar() {
        int id = Integer.parseInt(txtId.getText());
        int id_emprendedor = (int)(jComboIdEmprendedor.getSelectedItem());
        int id_pais = (int)(jComboIdPais.getSelectedItem());
        String nombre = txtNombre.getText();
        String sector = txtSectorI.getText();
        double avaluo = Double.parseDouble(txtAvaluo.getText());
        
        
        Proyecto proyecto = new Proyecto(id,id_emprendedor,id_pais,nombre,sector,avaluo);
        try {
            proyectoController.actualizar(proyecto);
            JOptionPane.showMessageDialog(this, "Registro Actualizado");
            listar();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    private void consultar() throws SQLException {
        int id = Integer.parseInt(txtBuscarID.getText());
        proyecto = proyectoController.consultar(id);
        if (proyecto == null) {
            JOptionPane.showMessageDialog(this, "ID NO ENCONTRADO");
        } else {
            txtId.setText(String.valueOf(proyecto.getId_proyecto()));
            jComboIdEmprendedor.setSelectedItem(proyecto.getId_emprendedor());
            jComboIdPais.setSelectedItem(proyecto.getId_pais_creacion());
            txtNombre.setText(proyecto.getNombre());
            txtSectorI.setText(proyecto.getSector_industrial());
            txtAvaluo.setText(String.valueOf(proyecto.getAvaluo_proyecto()));
            
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
            btnAgregar.setEnabled(false);
        }
    }

    private void limpiarFormulario() {
        txtId.setText("");
//        jComboIdPais.setText("");
//        jComboIdUsuario.setText("");
        txtNombre.setText("");
        txtSectorI.setText("");
        txtAvaluo.setText("");
        txtBuscarID.setText("");
        inicializarBotonera();
    }

    private void inicializarBotonera() {
        btnAgregar.setEnabled(false);
        btnBuscar.setEnabled(false);
        
        if(this.tblTabla.getSelectedRow()>0 || this.tblTabla.getRowCount()>0){
            btnActualizar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }else{
            btnActualizar.setEnabled(false);
            btnEliminar.setEnabled(false);
        }
    }

    private boolean validarVacios() {
        boolean validado = false;
        if ((!txtNombre.getText().trim().isEmpty())&& (!txtSectorI.getText().trim().isEmpty())&& (!txtAvaluo.getText().trim().isEmpty())) {
            validado = true;
        }
        return validado;
    }
    
    private void agregar() {

        
        
        int id_emprendedor = (int)(jComboIdEmprendedor.getSelectedItem());
        int id_pais = (int)(jComboIdPais.getSelectedItem());
        String nombre = txtNombre.getText();
        String sector = txtSectorI.getText();
        double avaluo = Double.parseDouble(txtAvaluo.getText());
        
        
        Proyecto proyecto = new Proyecto(0,id_emprendedor,id_pais,nombre,sector,avaluo);
        try {
            proyectoController.agregar(proyecto);
            JOptionPane.showMessageDialog(this, "Registro agregado");
            listar();
            limpiarFormulario();
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

