    public void listar() throws SQLException {
        tableModel.setRowCount(0); // Limpiar la tabla
        List<Proyecto> listado = proyectoController.listar();
        listado.forEach((proyecto) -> {
            tableModel.addRow(new Object[]{proyecto.getId_proyecto(), proyecto.getId_emprendedor(), proyecto.getId_pais_creacion(), proyecto.getNombre(), proyecto.getSector_industrial(), proyecto.getAvaluo_proyecto()});
        });
    }
    
    public void listar_paises(List<Integer> listado_paises,JComboBox c) throws SQLException{
        DefaultComboBoxModel combo=new DefaultComboBoxModel();
        c.setModel(combo);
        for (int i = 0; i < listado_paises.size(); i++) {
            combo.addElement(listado_paises.get(i));
            
        }
    }
    
    public void listar_emprendedores(List<Integer> listado_emprendedores,JComboBox c) throws SQLException{
        DefaultComboBoxModel combo=new DefaultComboBoxModel();
        c.setModel(combo);
        for (int i = 0; i < listado_emprendedores.size(); i++) {
            combo.addElement(listado_emprendedores.get(i));
            
        }
    }

